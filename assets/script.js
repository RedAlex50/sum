function openFormMenu() {
    document.getElementById('btn_openMenuForm').style.display = "none";
    document.getElementById('menu_form').style.display = "block";
}
function closeFormMenu() {
    document.getElementById('menu_form').style.display = "none";
    document.getElementById('btn_openMenuForm').style.display = "block";
}

function openItemsMenu() {
    document.getElementById('items_form').style.display = "block";
}
function closeItemsMenu() {
    document.getElementById('items_form').style.display = "none";
}